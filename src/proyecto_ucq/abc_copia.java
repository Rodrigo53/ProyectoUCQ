    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        con = new conectar();
       Connection cn = con.getConnection();   
       String sql = "UPDATE tusuario SET Nombre_Usuario='" + this.txt_Nombre.getText() + "',"
                                 + "Usuario='" + this.txt_Usuario.getText() + "',"
                                 + "Clave='" + this.ps_contra.getText() + "',"
                                 + "Correo_personal='" + this.txt_CorreoP.getText() + "',"
                                 + "Correo_institucional='" + this.txt_CorreoI.getText() + "',"
                                 + "Telefono_fijo='" + this.txt_Telefono1.getText() + "',"
                                 + "Estatus='" + this.cmb_estatus.getSelectedItem().toString() + "',"
                                 + "Carrera='" + this.cmb_carrera.getSelectedItem().toString() + "',"
                                 + "Fotografia='" + this.lbfoto.getIcon() + "',"
                                 + "Telefono_celular='" + this.txt_Telefono2.getText() + "' WHERE Id_Usuario='" + this.txt_clave.getText() + "'";
      
       try {
            PreparedStatement pps = (PreparedStatement) cn.prepareStatement(sql);
        pps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Datos Actualizados", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
            mostrarDatos("");
        } catch (SQLException ex) {
              JOptionPane.showMessageDialog(null,"Error al Actualizar Datos", "Mensaje Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(abc_copia.class.getName()).log(Level.SEVERE, null, ex);
        }
        con.Desconectar(); 
        bloquear();
    }//GEN-LAST:event_btn_guardarActionPerformed

