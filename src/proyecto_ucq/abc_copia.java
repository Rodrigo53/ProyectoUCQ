    void mostrarDatos(String valor){
       con = new conectar();
       Connection cn = con.getConnection();
                //Cabezas de Tabla
            String [] titulos={"Clave  de Usuario","Nombre Completo de Usuario","Usuario","Contrase√±a","Tipo de Usuario","Estatus","Correo Personal","Correo Institucional","Telefono Fijo" ,"Telefono Celular","Carrera"};
            String [] registros = new String[11];
            
            String sql="SELECT * FROM tusuario WHERE CONCAT (Nombre_Usuario,'',Id_Usuario )LIKE '%"+valor+"%'";
            model = new DefaultTableModel(null,titulos);
           try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                registros[0]=rs.getString("Id_Usuario");
                registros[1]=rs.getString("Nombre_Usuario");
                registros[2]=rs.getString("Usuario");
                registros[3]=rs.getString("Clave");
                registros[4]=rs.getString("Tipo_Usuario");
                registros[5]=rs.getString("Estatus");
                registros[6]=rs.getString("Correo_personal");
                registros[7]=rs.getString("Correo_institucional");
                registros[8]=rs.getString("Telefono_fijo");
                registros[9]=rs.getString("Telefono_celular");
                registros[10]=rs.getString("Carrera");
               model.addRow(registros);
            }
            tb_usuarios.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex );
        }
         con.Desconectar();  
    }
