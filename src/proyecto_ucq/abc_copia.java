    private void Btn_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_AgregarActionPerformed
       con = new conectar();
       Connection cn = con.getConnection();
        try{
            FileInputStream  archivofoto;
            if(txt_clave.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese la Clave de Usuario","error",JOptionPane.ERROR_MESSAGE); 
            }
             else if(txt_Nombre.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese el Nombre","error",JOptionPane.ERROR_MESSAGE); 
            }
            
             else if(txt_Usuario.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese el Usuario","error",JOptionPane.ERROR_MESSAGE); 
            }
             else if(ps_contra.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese la Contrase√±a","error",JOptionPane.ERROR_MESSAGE); 
             
             }else if(txt_Telefono1.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese el Telefono Fijo","error",JOptionPane.ERROR_MESSAGE); 
            }else if(txt_Telefono2.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese el Telefono Movil","error",JOptionPane.ERROR_MESSAGE); 
            
            }else if(txt_CorreoP.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese el Correo Personal Fijo","error",JOptionPane.ERROR_MESSAGE); 
            
            }else if(txt_CorreoI.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Ingrese el Correo Institucional","error",JOptionPane.ERROR_MESSAGE); 
            }else{
                
            
            PreparedStatement spt=cn.prepareStatement("INSERT INTO tusuario (Id_Usuario,Nombre_Usuario,Usuario,Clave,Tipo_Usuario,Estatus,Ruta,Fotografia,Correo_personal,Correo_institucional,Telefono_fijo,Telefono_celular,Carrera) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?)");
            spt.setString(1, txt_clave.getText());
            spt.setString(2, txt_Nombre.getText());
            spt.setString(3, txt_Usuario.getText());
            spt.setString(4, ps_contra.getText());
            spt.setString(5, cmb_tu1.getSelectedItem().toString());
            spt.setString(6, cmb_estatus.getSelectedItem().toString());
            archivofoto = new FileInputStream(txt_Imagen.getText());
            spt.setBinaryStream(7, archivofoto );
            spt.setString(8, txt_CorreoP.getText());
            spt.setString(9, txt_CorreoI.getText());
            spt.setString(10,txt_Telefono1.getText());
            spt.setString(11,txt_Telefono2.getText());
            spt.setString(12,cmb_carrera.getSelectedItem().toString());
            spt.setString(13, txt_Imagen.getText());
            int N=spt.executeUpdate();
            if (N>0){
            JOptionPane.showMessageDialog(null,"Registro Guardado Exitosamente");
            }  }
            mostrarDatos("");
            }catch(Exception e){
                System.out.print(e.getMessage());
                                    }
        limpiar();
         con.Desconectar();    
    }//GEN-LAST:event_Btn_AgregarActionPerformed

